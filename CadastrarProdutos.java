    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Produtos cl= new Produtos();
        ManterProdutos dao = new ManterProdutos();
        cl.setId_produto(jTidproduto.getText());
        cl.setCategoria_id(Integer.valueOf(jTcategoria.getText()));
        cl.setProduto(jTproduto.getText());
        cl.setPreco_R$(Float.valueOf(jTpreco.getText()));
        cl.setQuantidade(Integer.valueOf(jTquant.getText()));
        cl.setTamanho(jTtamanho.getText());
        try {
            dao.inserir(cl);
        } catch (Exception ex) {
            Logger.getLogger(CadastrarProdutos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

