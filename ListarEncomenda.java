    private void jBlistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBlistarActionPerformed
        // TODO add your handling code here:
        try {
           Encomenda enc = new Encomenda();
        ManterEncomenda manterEncomenda = new ManterEncomenda();

        DefaultTableModel modeloTabela = (DefaultTableModel) jTctdr.getModel();
        modeloTabela.setRowCount(0);

        if (jTcpf.getText().isEmpty()) {
            ArrayList<Encomenda> encomendas = manterEncomenda.PesquisarTudo();

            encomendas.forEach((e) -> {
                modeloTabela.addRow(new Object[]{e.getCodigo(), e.getCpf(), e.getData_encomenda()});
            });
        } else {
            enc.setCodigo(jTcpf.getText());
            manterEncomenda.PesquisarRegistro(enc);

            if (enc.getCpf() != 0) {
                modeloTabela.addRow(new Object[]{enc.getCodigo(), enc.getCpf(), enc.getData_encomenda()});
            } else {
                System.out.println("Encomenda n√£o encontrada.");
            }
        }

            jTcpf.setText(null);
            jTcpf.requestFocus();
        }
        catch (Exception e) {
            System.out.println("Erro" + e.getMessage());
        }

    }//GEN-LAST:event_jBlistarActionPerformed

