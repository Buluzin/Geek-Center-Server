    private void jBalterar16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBalterar16ActionPerformed
        try{

            Produtos c = new Produtos();
            ManterProdutos dao = new ManterProdutos();
            c.setId_produto(jTidproduto.getText());
            c.setCategoria_id(Integer.valueOf(jTidcategoria.getText()));
            c.setProduto(jTproduto.getText());
            c.setPreco_R$(Float.parseFloat(jTpreco.getText()));
            c.setQuantidade(Integer.valueOf(jTquant.getText()));
            c.setTamanho(jTtamanho.getText());
            dao.editarProdutos(c);

            jTproduto.setText("");
            jTidcategoria.setText("");
            jTpreco.setText("");
            jTquant.setText("");
            jTtamanho.setText("");
            jTidproduto.setText("");
            jTidproduto.requestFocus();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Erro de : " + e.getMessage());
        }
    }//GEN-LAST:event_jBalterar16ActionPerformed

