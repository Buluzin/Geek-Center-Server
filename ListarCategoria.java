    private void jBlistarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBlistarActionPerformed
        // TODO add your handling code here:
        try {
            Categoria ca = new Categoria();
            ManterCategoria cc = new ManterCategoria();

            DefaultTableModel modeloTabela = (DefaultTableModel) jTctdr.getModel();
            modeloTabela.setRowCount(0);

            if (jTid.getText().isEmpty()) {
                 ArrayList<Categoria> categorias = cc.PesquisarTudo();
                    categorias.forEach((categoria) -> {
        modeloTabela.addRow(new Object[]{categoria.getId_categoria(), categoria.getNome()});
    });
        } else {
           ca.setId_categoria(Integer.parseInt(jTid.getText()));
           cc.PesquisarRegistro(ca);

            if (ca.getNome() != null) {
                modeloTabela.addRow(new Object[]{ca.getId_categoria(), ca.getNome()});
            } else {
            JOptionPane.showMessageDialog(null, "Categoria n√£o encontrada.");
    }
        }

            jTid.setText(null);
            jTid.requestFocus();
        }
        catch (Exception e) {
            System.out.println("Erro" + e.getMessage());
        }

    }//GEN-LAST:event_jBlistarActionPerformed

